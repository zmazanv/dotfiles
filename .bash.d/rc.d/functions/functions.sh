# Source script to enable lfcd alias. It opens lf, but will change working
# directory in shell to last directory in lf on exit.
#-------------------------------------------------------------------------
if [[ -f "$HOME/.config/lf/scripts/lfcd.sh" ]]; then
	LFCD="$HOME/.config/lf/scripts/lfcd.sh"
	source "${LFCD}"
elif [[ -f '/etc/profile.d/lfcd.sh' ]]; then
	LFCD='/etc/profile.d/lfcd.sh'
	source "${LFCD}"
fi
