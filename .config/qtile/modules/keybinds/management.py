from libqtile.config import Key
from libqtile.lazy import lazy
# ../variables/keybinds.py
from ..variables.keybinds import mod
###############################################################################
# # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # # #


keys = [
    ###########################################################################
    # RECARGAR
    ###########################################################################
    # RELOAD
    ###########################################################################
    # -------------------------------------------------------------------------
    # Recargará la configuración.
    # -------------------------------------------------------------------------
    # Will reload the configuration.
    # -------------------------------------------------------------------------
    # SUPER + CONTROL + r
    Key(
        [mod, 'control'],
        'r',
        lazy.reload_config(),
        desc='Reload the config',
    ),
    # -------------------------------------------------------------------------
    ###########################################################################


    ###########################################################################
    # FINALIZACIÓN
    ###########################################################################
    # SHUTDOWN
    ###########################################################################
    # -------------------------------------------------------------------------
    # Finalizará Qtile.
    # -------------------------------------------------------------------------
    # Will shutdown Qtile.
    # -------------------------------------------------------------------------
    # SUPER + CONTROL + q
    Key(
        [mod, 'control'],
        'q',
        lazy.shutdown(),
        desc='Shutdown Qtile',
    ),
    # -------------------------------------------------------------------------
    ###########################################################################
]
